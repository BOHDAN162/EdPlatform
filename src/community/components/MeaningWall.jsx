const formatTimeAgo = (date) => {
  const now = Date.now();
  const value = typeof date === "string" ? Date.parse(date) : date;
  if (!value) return "—Ç–æ–ª—å–∫–æ —á—Ç–æ";
  const diff = Math.max(1, Math.floor((now - value) / 1000));
  const minutes = Math.floor(diff / 60);
  if (minutes < 1) return "—Ç–æ–ª—å–∫–æ —á—Ç–æ";
  if (minutes < 60) return `${minutes} –º–∏–Ω –Ω–∞–∑–∞–¥`;
  const hours = Math.floor(minutes / 60);
  if (hours < 24) return `${hours} —á –Ω–∞–∑–∞–¥`;
  const days = Math.floor(hours / 24);
  if (days < 7) return `${days} –¥–Ω –Ω–∞–∑–∞–¥`;
  const weeks = Math.floor(days / 7);
  return `${weeks} –Ω–µ–¥ –Ω–∞–∑–∞–¥`;
};

const createGeneratedPosts = (startIndex, count = 12) => {
  const generated = [];
  for (let i = 0; i < count; i += 1) {
    const base = seedPosts[(startIndex + i) % seedPosts.length];
    const offsetMinutes = (startIndex + i) * 9 + Math.floor(Math.random() * 20);
    generated.push({
      ...base,
      id: `gen-${startIndex + i}-${Date.now()}`,
      createdAt: new Date(Date.now() - offsetMinutes * 60 * 1000).toISOString(),
      likes: Math.max(1, (base.likes || 0) + (i % 2 === 0 ? 1 : -2) + Math.floor(Math.random() * 4)),
      saves: Math.max(0, (base.saves || 0) + Math.floor(Math.random() * 3)),
      liked: false,
      saved: false,
    });
  }
  return generated;
};

